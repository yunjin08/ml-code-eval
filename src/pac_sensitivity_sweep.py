"""
PaC sensitivity sweep: vary PaC weight (beta) and optional PaC signal gain, re-tune
Block/Review thresholds on validation for each setting, then evaluate on the test set.

Requires (from a full Phase 3 run):
  - results/phase3_experiment_results.csv
  - results/phase3_hybrid_config.json
  - results/phase3_validation_scores.csv

This answers: "As PaC influence increases, where does Hybrid F1 / ΔAUC peak or plateau?"
"""

from __future__ import annotations

import argparse
import json
import os

import numpy as np
import pandas as pd
from sklearn.metrics import f1_score, precision_score, recall_score, roc_auc_score

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
PROJECT_ROOT = os.path.dirname(SCRIPT_DIR)
RESULTS_DIR = os.path.join(PROJECT_ROOT, "results")


def _normalize_scores(scores: np.ndarray, s_min: float, s_max: float, eps: float = 1e-9) -> np.ndarray:
    span = s_max - s_min + eps
    return np.clip((scores - s_min) / span, 0.0, 1.0)


def tune_thresholds_on_val_for_hybrid(
    hybrid_val: np.ndarray,
    labels: np.ndarray,
) -> tuple[float, float, float]:
    """Grid-search t_block, t_review to maximize F1 for Block (hybrid >= t_block vs label)."""
    best_f1 = -1.0
    best_t_block = 0.4
    best_t_review = 0.1
    for t_block in [0.3, 0.4, 0.5, 0.6, 0.7]:
        for t_review in [0.1, 0.2, 0.25, 0.3]:
            if t_review >= t_block:
                continue
            block_pred = (hybrid_val >= t_block).astype(int)
            f1 = float(f1_score(labels, block_pred, zero_division=0))
            if f1 > best_f1:
                best_f1 = f1
                best_t_block = t_block
                best_t_review = t_review
    return best_t_block, best_t_review, best_f1


def block_binary_from_score(scores: np.ndarray, t_block: float) -> np.ndarray:
    return (scores >= t_block).astype(int)


def _write_tex_artifacts(project_root: str, out_df: pd.DataFrame, summary: dict) -> None:
    """Write tex/pac_sensitivity_gain1.dat and tex/pac_sensitivity_constants.tex (peak macros only)."""
    tex_dir = os.path.join(project_root, "tex")
    os.makedirs(tex_dir, exist_ok=True)

    sub = out_df[out_df["pac_gain"] == 1.0].sort_values("beta")
    dat_path = os.path.join(tex_dir, "pac_sensitivity_gain1.dat")
    with open(dat_path, "w", encoding="utf-8") as f:
        f.write("beta f1 delta_auc\n")
        for _, row in sub.iterrows():
            f.write(
                f"{float(row['beta']):.10f} {float(row['test_f1_block']):.10f} "
                f"{float(row['test_delta_auc_hybrid_minus_ml']):.10f}\n"
            )

    peaks = summary.get("peaks_by_pac_gain") or []
    pk1 = next((p for p in peaks if float(p["pac_gain"]) == 1.0), None)
    ref_beta = float(summary.get("reference_phase3_beta", -1))

    def _fmt(x: float) -> str:
        return f"{x:.4f}".rstrip("0").rstrip(".")

    const_lines = [
        "% Auto-generated by src/pac_sensitivity_sweep.py — do not edit by hand.",
        "% Re-run: python src/pac_sensitivity_sweep.py --pac-gains 1.0,1.5,2.0",
        "% Sensitivity figure curves: explicit \\addplot coordinates in manuscript.tex (Figure pac_sensitivity).",
        "% If sweep data changes, update those coordinates from pac_sensitivity_gain1.dat or this CSV.",
    ]
    if pk1:
        p_f1 = pk1["peak_test_f1_block"]
        p_d = pk1["peak_test_delta_auc"]
        const_lines.extend(
            [
                f"\\providecommand{{\\PacSensPeakFOneBeta}}{{{_fmt(float(p_f1['beta']))}}}",
                f"\\providecommand{{\\PacSensPeakFOne}}{{{float(p_f1['test_f1_block']):.4f}}}",
                f"\\providecommand{{\\PacSensPeakDeltaAucBeta}}{{{_fmt(float(p_d['beta']))}}}",
                f"\\providecommand{{\\PacSensPeakDeltaAuc}}{{{float(p_d['test_delta_auc']):.4f}}}",
            ]
        )
    const_lines.append(f"\\providecommand{{\\PacSensPhaseThreeBeta}}{{{_fmt(ref_beta)}}}")

    const_path = os.path.join(tex_dir, "pac_sensitivity_constants.tex")
    with open(const_path, "w", encoding="utf-8") as f:
        f.write("\n".join(const_lines) + "\n")

    print(f"Wrote {dat_path}")
    print(f"Wrote {const_path}")


def run_sweep_for_gain(
    val_ml_n: np.ndarray,
    val_pac_n: np.ndarray,
    val_labels: np.ndarray,
    test_ml_n: np.ndarray,
    test_pac_n: np.ndarray,
    y_true: np.ndarray,
    auc_ml: float,
    betas: np.ndarray,
    pac_gain: float,
) -> pd.DataFrame:
    val_pac_eff = np.clip(val_pac_n * pac_gain, 0.0, 1.0)
    test_pac_eff = np.clip(test_pac_n * pac_gain, 0.0, 1.0)
    rows = []
    for beta in betas:
        alpha = float(1.0 - beta)
        hybrid_val = alpha * val_ml_n + beta * val_pac_eff
        t_block, t_review, val_f1 = tune_thresholds_on_val_for_hybrid(hybrid_val, val_labels)

        hybrid_test = alpha * test_ml_n + beta * test_pac_eff
        pred_block = block_binary_from_score(hybrid_test, t_block)

        p = float(precision_score(y_true, pred_block, zero_division=0))
        r = float(recall_score(y_true, pred_block, zero_division=0))
        f1 = float(f1_score(y_true, pred_block, zero_division=0))
        auc_h = float(roc_auc_score(y_true, hybrid_test))
        delta_auc = auc_h - auc_ml

        rows.append(
            {
                "alpha": alpha,
                "beta": beta,
                "pac_gain": float(pac_gain),
                "t_block": t_block,
                "t_review": t_review,
                "val_f1_block_tuned": val_f1,
                "test_precision_block": p,
                "test_recall_block": r,
                "test_f1_block": f1,
                "test_auc_hybrid": auc_h,
                "test_auc_ml_baseline": auc_ml,
                "test_delta_auc_hybrid_minus_ml": delta_auc,
            }
        )
    return pd.DataFrame(rows)


def main():
    parser = argparse.ArgumentParser(description="Sweep PaC weight beta (and optional PaC gain) for hybrid risk.")
    parser.add_argument("--steps", type=int, default=21, help="Number of beta values from beta-min to beta-max (inclusive).")
    parser.add_argument("--beta-min", type=float, default=0.0, dest="beta_min")
    parser.add_argument("--beta-max", type=float, default=1.0, dest="beta_max")
    parser.add_argument(
        "--pac-gain",
        type=float,
        default=1.0,
        help="Single run: multiply normalized PaC by this factor before clipping. Ignored if --pac-gains is set.",
    )
    parser.add_argument(
        "--pac-gains",
        default=None,
        help="Comma-separated list, e.g. 1.0,1.5,2.0. Runs one sweep per gain (rows concatenated in CSV).",
    )
    parser.add_argument(
        "--results-dir",
        default=RESULTS_DIR,
        help="Directory containing phase3 outputs.",
    )
    args = parser.parse_args()
    rdir = args.results_dir

    test_path = os.path.join(rdir, "phase3_experiment_results.csv")
    cfg_path = os.path.join(rdir, "phase3_hybrid_config.json")
    val_path = os.path.join(rdir, "phase3_validation_scores.csv")

    for p in (test_path, cfg_path, val_path):
        if not os.path.isfile(p):
            raise FileNotFoundError(
                f"Missing {p}. Run Phase 3 first (it writes phase3_validation_scores.csv). "
                "If you have an older Phase 3 run, re-run: python src/phase3_experiment.py"
            )

    with open(cfg_path) as f:
        cfg = json.load(f)

    val_ml_min = float(cfg["val_ml_min"])
    val_ml_max = float(cfg["val_ml_max"])
    val_pac_min = float(cfg["val_pac_min"])
    val_pac_max = float(cfg["val_pac_max"])

    val_df = pd.read_csv(val_path)
    test_df = pd.read_csv(test_path)

    val_ml = val_df["ml_confidence"].values
    val_pac = val_df["pac_score"].values
    val_labels = val_df["label"].values

    test_ml = test_df["ml_confidence"].values
    test_pac = test_df["pac_score"].values
    y_true = test_df["label"].values

    val_ml_n = _normalize_scores(val_ml, val_ml_min, val_ml_max)
    val_pac_n = _normalize_scores(val_pac, val_pac_min, val_pac_max)

    test_ml_n = _normalize_scores(test_ml, val_ml_min, val_ml_max)
    test_pac_n = _normalize_scores(test_pac, val_pac_min, val_pac_max)

    auc_ml = float(roc_auc_score(y_true, test_ml))

    betas = np.linspace(args.beta_min, args.beta_max, args.steps)

    if args.pac_gains:
        gains = [float(x.strip()) for x in args.pac_gains.split(",") if x.strip()]
    else:
        gains = [float(args.pac_gain)]

    frames = []
    for g in gains:
        frames.append(
            run_sweep_for_gain(
                val_ml_n,
                val_pac_n,
                val_labels,
                test_ml_n,
                test_pac_n,
                y_true,
                auc_ml,
                betas,
                g,
            )
        )
    out_df = pd.concat(frames, ignore_index=True)
    csv_out = os.path.join(rdir, "pac_sensitivity_sweep.csv")
    out_df.to_csv(csv_out, index=False)

    peaks_by_gain = []
    for g in gains:
        sub = out_df[out_df["pac_gain"] == g]
        if sub.empty:
            continue
        i_max_f1 = int(sub["test_f1_block"].values.argmax())
        i_max_dauc = int(sub["test_delta_auc_hybrid_minus_ml"].values.argmax())
        peaks_by_gain.append(
            {
                "pac_gain": g,
                "peak_test_f1_block": {
                    "beta": float(sub.iloc[i_max_f1]["beta"]),
                    "test_f1_block": float(sub.iloc[i_max_f1]["test_f1_block"]),
                    "t_block": float(sub.iloc[i_max_f1]["t_block"]),
                },
                "peak_test_delta_auc": {
                    "beta": float(sub.iloc[i_max_dauc]["beta"]),
                    "test_delta_auc": float(sub.iloc[i_max_dauc]["test_delta_auc_hybrid_minus_ml"]),
                },
            }
        )

    summary = {
        "pac_gains_evaluated": gains,
        "beta_grid": {"min": args.beta_min, "max": args.beta_max, "steps": args.steps},
        "auc_ml_test": auc_ml,
        "peaks_by_pac_gain": peaks_by_gain,
        "reference_phase3_beta": float(cfg.get("beta", -1)),
        "interpretation": (
            "For each (pac_gain, beta), thresholds are re-tuned on validation to maximize Block F1 (same grid as Phase 3). "
            "Increasing beta gives PaC more weight in hybrid_risk = alpha*ML_norm + beta*PaC_norm. "
            "pac_gain>1 simulates a stronger PaC channel (e.g., richer rules) without re-running Semgrep. "
            "To measure real rule-set expansion, re-run Phase 3 with a larger Semgrep config and compare peaks across runs."
        ),
    }

    json_out = os.path.join(rdir, "pac_sensitivity_sweep.json")
    with open(json_out, "w") as f:
        json.dump(summary, f, indent=2)

    _write_tex_artifacts(PROJECT_ROOT, out_df, summary)

    print(f"Wrote {csv_out}")
    print(f"Wrote {json_out}")
    for pk in peaks_by_gain:
        print(
            f"[pac_gain={pk['pac_gain']}] peak F1 at beta={pk['peak_test_f1_block']['beta']:.4f}, "
            f"F1={pk['peak_test_f1_block']['test_f1_block']:.4f}; "
            f"peak ΔAUC at beta={pk['peak_test_delta_auc']['beta']:.4f}, "
            f"Δ={pk['peak_test_delta_auc']['test_delta_auc']:.4f}"
        )
    print(f"Phase 3 chosen beta was: {summary['reference_phase3_beta']}")


if __name__ == "__main__":
    main()
